//Utilizando >= 
console.log(5 >= 3); // Mostra no console true, porque 5 é maior que 3.
console.log(5 >= 5); // Mostra no console true, porque 5 não é maior, mais é igual a 5. 
console.log(5 >= 7); // Mostra no console false, porque 5 é menor que 7.

//Utilizando ==
console.log(5 == 3); // Mostra no console false, porque 5 não é equivalente a 3.
console.log(5 == 5); // Mostra no console true, porque 5 é equivalente a 5.

//Utilizando !=
console.log(5 != 3); // Mostra no console true, porque 5 é diferente de 3.
console.log(5 != 5); // Mostra no console false, porque 5 é igual a 5.

